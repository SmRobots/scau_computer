  // 主要是要判断哪些是指针
	printf("%d\n", a[2][1]);
	printf("%d\n", (a + 1 - a) * 4 * 4 + 2000);
	printf("%d\n", (a - a) * 4 * 4 + 2000);
	printf("%d\n", (a + 1 - a) * 4 * 4 + 2000);
	printf("%d\n", (*a + 1 - a[0]) * 4 + 2000);
